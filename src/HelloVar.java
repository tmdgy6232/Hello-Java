// Varriable : Var
public class HelloVar {

	public static void main(String[] args) {
		
		
		// 숫자 30을 기억장치에 저장하고 기억장치에 num라는 이름표를 붙힌다.
		int num = 30;
		
		// 1. 30+40의 계산식을 기억장치에 저장하고 
		// 2. 기억장치에 sum이라는 이름표를 부착
		
		// ㄱ. 실제로는 30을 임의의 기억장치에 저장하고
		// ㄴ. 40을 임의의 기억장치에 저장하고
		// ㄷ. + 기호를 만나면
		// ㄹ. 연산장치에서 30과 40을 불러와 연산을 수행하고
		// ㅂ. 두 수의 결과값을 기억장치에 저장하고
		// ㅅ. 결과가 저장된 기억장치에 sum이라는 이름표를 부착
		
		
		int sum = 30 + 40;
		
		
		System.out.println(num + sum);

	}

}
